


$(document).ready(function () 
{
    $('#dropdown-display').hover(function(){
		$('#dropdown-content').show();
    }, function() {
        $('#dropdown-content').hide();
    });
    $('#dropdown-display-community').hover(function(){
		$('#dropdown-content-community').show();
    }, function() {
        $('#dropdown-content-community').hide();
    });    
});

